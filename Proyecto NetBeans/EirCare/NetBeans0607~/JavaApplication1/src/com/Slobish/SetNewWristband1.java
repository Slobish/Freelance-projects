public String generatePassword()
{
   Random randGen = new Random();
    String pass ="";

    for (int i = 0; i < 8; i++)
    {
        if (randGen.nextInt(2) == 0)
        {
            pass=pass+((char)(randGen.nextInt( 'Z' - 'A' + 1  ) + 65));
        }
        else
        {
            pass=pass+((char)(randGen.nextInt( '9' - '0' + 1 ) + 48));
        }
    }
    //System.out.println(randGen.nextInt( ('Z' - 'A' + 1 ) + 1));
    return pass;
}



        
